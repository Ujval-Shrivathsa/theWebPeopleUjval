function openModal(){
    const video = document.querySelector('.iframeVideo');
    const close = document.querySelector('.thirdContainer');
    close.style.display = 'none';
    video.style.display = 'block';
}

function closeModal(){
    const video = document.querySelector('.iframeVideo');
    const close = document.querySelector('.thirdContainer');
    close.style.display = 'block';
    video.style.display = 'none';
}

